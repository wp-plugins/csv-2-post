<h2>Stage 4 Instructions
</h2>
<h3>Rows 1,2,3</h3>
<p>Using these rows will assign the same values to every post which is handy for some sort of identifier in this campaign&nbsp;which is also used in your theme</p>
<h3>Rows 4,5,6</h3>
<p>These rows allow you to pair your &quot;Identifier&quot; with one of your CSV file columns! This gives you the ability to assign a column that has unique or almost unique values per row. </p>
<h3>Identifier</h3>
<p>This should be a single string with no spaces. It is the part of a Custom Field that will be used by your theme or other plugins.</p>
<h3>Assigned Value</h3>
<p>This is the value that will be used in your theme or other plugins in order to make it work as expected. </p>
